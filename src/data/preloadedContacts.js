// Helper function to get random contacts for testing
export const getRandomContacts = (count = 10) => {
  const shuffled = [...preloadedContacts].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Helper function to add preloaded contacts to your app
export const addPreloadedContactsToFirestore = async (user, addBrother) => {
  if (!user || !addBrother) {
    console.error('User or addBrother function not provided');
    return;
  }

  try {
    console.log('Adding preloaded contacts...');
    
    for (const contact of preloadedContacts) {
      const brotherData = {
        name: contact.name,
        phone: contact.phone,
        email: contact.email,
        location: contact.location,
        bio: contact.bio,
        notes: contact.notes,
        lastContact: null,
        lastContactMethod: null,
        preferences: {
          preferredContact: 'phone',
          notifications: true
        }
      };

      await addBrother(brotherData);
      
      // Small delay to avoid overwhelming the database
      await new Promise(resolve => setTimeout(resolve, 100));
    }
    
    console.log('Successfully added all 40 preloaded contacts!');
    return { success: true, message: 'All contacts added successfully!' };
  } catch (error) {
    console.error('Error adding preloaded contacts:', error);
    return { success: false, error: error.message };
  }
};